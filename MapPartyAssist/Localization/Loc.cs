        internal static string Tr(string fallback) {
            if(string.IsNullOrEmpty(fallback)) {
                return fallback;
            }

            if(Translations.TryGetValue(fallback, out var translated) && !string.IsNullOrEmpty(translated)) {
                return translated;
            }

            return fallback;
        }

        internal static string TrFormat(string fallback, params object[] args) {
            return string.Format(CultureInfo.CurrentCulture, Tr(fallback), args);
        }
